	int Application::Initialize(int argumentCount, char** arguments)
	{
		Math::TestMatrixMultiplication();
        //Math::VectorTest();
		
		if (int errCode = SDL_Init(SDL_INIT_VIDEO) != 0)
		{
#ifdef _WIN32
			fprintf_s(stdout, SDL_GetError());
#else
            fprintf(stdout, "%s", SDL_GetError());
#endif
			SDL_Quit();
			return errCode;
		}

		SDL_Window* m_window = SDL_CreateWindow("Test Window", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, k_windowWidth, k_windowHeight, SDL_WINDOW_SHOWN);

		if (m_window == nullptr)
		{
#ifdef _WIN32
            fprintf_s(stdout, SDL_GetError());
#else
            fprintf(stdout, "%s", SDL_GetError());
#endif
			SDL_Quit();
			return -1;
		}

		SDL_SysWMinfo wmInfo;
		SDL_VERSION(&wmInfo.version);
		SDL_GetWindowWMInfo(m_window, &wmInfo);

		// Calling this here prevents the creation of a separate bgfx backend render thread, apparently?
		bgfx::renderFrame();

		Renderer::RendererArgs renderArgs;
#ifdef _WIN32
		renderArgs.platformData.nwh = wmInfo.info.win.window;
#else
        renderArgs.platformData.nwh = (void*)(wmInfo.info.x11.window);
#endif
		renderArgs.width = (uint32_t)k_windowWidth;
		renderArgs.height = (uint32_t)k_windowHeight;

		m_renderer = &(Renderer::Create(renderArgs));

		m_initialized = true;

		return 0;
	}

	int Application::Run()
	{
		assert(m_initialized);

		bool running = true;
		while (running)
		{
			SDL_Event e;
			while (SDL_PollEvent(&e))
			{
				switch (e.type)
				{
				case SDL_WINDOWEVENT:
					if (e.window.event == SDL_WINDOWEVENT_CLOSE)
					{
						m_renderer->PushEvent(new Renderer::ExitEvent);
						running = false;
					}
					if (e.window.event == SDL_WINDOWEVENT_RESIZED)
					{
						m_renderer->PushEvent(new Renderer::ResizeEvent{ Renderer::EventType::Resize, e.window.data1, e.window.data2 });
					}
					break;
				case SDL_KEYDOWN:
				case SDL_KEYUP:
					if (e.key.keysym.scancode == SDLK_q)
					{
						running = false;
					}
					m_renderer->PushEvent(new Renderer::KeyEvent{ Renderer::EventType::Key, e.key.keysym.scancode, e.type });
					break;
				}
			}
			bgfx::renderFrame();
		}

		while (auto result = bgfx::renderFrame())
		{
			if (bgfx::RenderFrame::NoContext == result || bgfx::RenderFrame::Timeout == result)
			{
				break;
			}
		}
		Renderer::Destroy();

		return 0;
	}

	bool Application::Shutdown()
	{
		SDL_Quit();
		if (m_window != nullptr)
		{
			SDL_DestroyWindow(m_window);
			m_window = nullptr;
		}

		m_initialized = false;
		return true;
	}
